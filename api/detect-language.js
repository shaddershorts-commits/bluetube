// Language map
const COUNTRY_LANG_MAP = COUNTRY_LANG;

export default async function handler(req, res) {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'GET, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
  if (req.method === 'OPTIONS') return res.status(200).end();

  try {
    const ip = (req.headers['x-forwarded-for'] || req.socket?.remoteAddress || '').split(',')[0].trim();
    let country = 'BR';
    let langCode = 'pt';

    if (ip && ip !== '127.0.0.1' && ip !== '::1' && !ip.startsWith('192.') && !ip.startsWith('10.')) {
      try {
        const geoRes = await fetch(`https://ipapi.co/${ip}/country/`, {
          headers: { 'User-Agent': 'BlueTube/1.0' }
        });
        if (geoRes.ok) {
          const text = (await geoRes.text()).trim().toUpperCase();
          if (text.length === 2) {
            country = text;
            langCode = COUNTRY_LANG_MAP[country] || 'pt';
          }
        }
      } catch (e) { /* default to pt */ }
    }

    const translations = T[langCode] || T['pt'];
    return res.status(200).json({ country, lang: langCode, translations });

  } catch (err) {
    return res.status(200).json({ country: 'BR', lang: 'pt', translations: T['pt'] });
  }
}
